"""Phase 33.1 — agent-server bundle builder.

Produces a self-contained Python source string that:

  1. Embeds ``WorkspaceStat`` (the only `app.tools.workspace` symbol
     the server uses).
  2. Embeds ``sha256_file`` (the only `app.tools.hash_ops` symbol).
  3. Embeds the full ``protocol.py`` content.
  4. Embeds the full ``server.py`` content.
  5. Reads ``AGENT_SERVER_PORT`` / ``AGENT_SERVER_TOKEN`` /
     ``AGENT_SERVER_WORKSPACE`` from env (or generates defaults).
  6. Calls ``_main()`` which binds the server + prints
     ``AGENT_SERVER_PORT/TOKEN/WORKSPACE`` lines to stdout, then
     serves until SIGTERM / SIGINT.

The bundle is meant to be fed to ``python3 -c "<bundle>"`` over
``docker exec -i`` or ``ssh``. No files written; no module structure.

§10.7: this is application-layer plumbing — no kernel imports added.
The bundled source comes from `app.tools.agent_server.protocol` +
`app.tools.agent_server.server` which already live in the tools
layer; this module assembles them into a `-c`-friendly script string.
"""

from __future__ import annotations

import hashlib
import re
from functools import lru_cache
from pathlib import Path

# Import statements we strip from the embedded source modules — the
# referenced symbols are inlined directly above in the bundle, so
# importing them via the in-package path would fail at runtime.
_STRIP_IMPORT_PREFIXES = (
    "from app.tools.agent_server.protocol",
    "from app.tools.hash_ops",
)


def _strip_app_imports(source: str) -> str:
    """Drop ``from app.*`` import statements (single- or multi-line)
    that reference symbols we've already inlined.

    Handles three syntactic shapes:

      1. ``from app.x import a``                 — single line
      2. ``from app.x import a, b``              — single line, multi-symbol
      3. ``from app.x import (\\n    a,\\n    b,\\n)`` — multi-line block
    """
    out_lines: list[str] = []
    lines = source.split("\n")
    i = 0
    while i < len(lines):
        line = lines[i]
        stripped = line.strip()
        # Is this the start of an import we want to drop?
        matches = any(stripped.startswith(prefix) for prefix in _STRIP_IMPORT_PREFIXES)
        if matches:
            # If the line opens a paren block, consume forward until
            # the matching close-paren line. Otherwise it's a single
            # line — skip it.
            if "(" in stripped and ")" not in stripped:
                i += 1
                while i < len(lines) and ")" not in lines[i]:
                    i += 1
                # Skip the closing-paren line too.
                if i < len(lines):
                    i += 1
            else:
                i += 1
            continue
        out_lines.append(line)
        i += 1
    return "\n".join(out_lines)


