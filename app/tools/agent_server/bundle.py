def _strip_module_docstring(source: str) -> str:
    """Remove the leading module docstring so the bundle keeps a single
    top-level docstring (the bundle header's)."""
    # Match the very-first triple-quoted block at module top, possibly
    # preceded by `from __future__ import annotations`.
    pattern = re.compile(
        r'^\s*(?:from __future__ import annotations\s*\n+)?"""[\s\S]*?"""\s*\n',
    )
    return pattern.sub("", source, count=1)


def _strip_future_import(source: str) -> str:
    """Remove `from __future__ import annotations` (already emitted in
    the bundle header)."""
    return re.sub(r"^from __future__ import annotations\s*\n", "", source, count=1)


def _strip_main_block(source: str) -> str:
    """Remove the in-module ``_main()`` + ``if __name__ == "__main__":``
    block in server.py — the bundle ships its own ``_main()`` wired to
    the inlined modules (see ``_BUNDLE_FOOTER`` below).

    Server.py's main block is preceded by a section divider using
    Unicode box-drawing characters (── module-as-entrypoint ──).
    Match that anchor + drop everything from it to end of file.
    """
    # Anchor: the section comment line containing "module-as-entrypoint".
    # Any preceding/following whitespace is fine; we want to drop from
    # the first occurrence onward.
    return re.sub(
        r"\n[^\n]*module-as-entrypoint[^\n]*\n.*$",
        "",
        source,
        flags=re.DOTALL,
    )


def _strip_stdlib_imports(source: str) -> str:
    """Remove stdlib import statements that the bundle header already
    contains, plus the typing/pydantic ones."""
    lines = source.split("\n")
    keep = []
    skip_prefixes = (
        "import base64",
        "import hashlib",
        "import json",
        "import os",
        "import re",
        "import secrets",
        "import shutil",
        "import signal",
        "import socket",
        "import sys",
        "import threading",
        "import urllib",
        "from collections.abc import",
        "from dataclasses import",
        "from http import",
        "from http.server import",
        "from pathlib import",
        "from typing import",
        "from pydantic import",
    )
    for line in lines:
        stripped = line.lstrip()
        if any(stripped.startswith(prefix) for prefix in skip_prefixes):
            continue
        keep.append(line)
    return "\n".join(keep)


def _module_source(module_path: Path) -> str:
    """Read + light-clean a Python module file for embedding."""
    text = module_path.read_text(encoding="utf-8")
    text = _strip_module_docstring(text)
    text = _strip_future_import(text)
    text = _strip_app_imports(text)
    text = _strip_stdlib_imports(text)
    text = _strip_main_block(text)
    # Collapse multiple blank lines.
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip() + "\n"


@lru_cache(maxsize=1)
def build_bundle() -> str:
    """Return the standalone Python source string. Cached because
    the inputs are static at process lifetime."""
    pkg_dir = Path(__file__).parent
    protocol_src = _module_source(pkg_dir / "protocol.py")
    server_src = _module_source(pkg_dir / "server.py")
    return (
        _BUNDLE_HEADER
        + "# ── inlined protocol.py ─────────────────────────────────────────────\n\n"
        + protocol_src
        + "\n# ── inlined server.py ───────────────────────────────────────────────\n\n"
        + server_src
        + _BUNDLE_FOOTER
    )


def bundle_sha256() -> str:
    """Hex digest of the bundled source — useful for cache keys + test
    pinning."""
    return hashlib.sha256(build_bundle().encode("utf-8")).hexdigest()


__all__ = ["build_bundle", "bundle_sha256"]
